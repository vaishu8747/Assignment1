a=input("enter the first no.")
b=input("enter the second no.")

#Sum of two number
sum=int(a)+int(b)
print('sum of {0} and {1} is {2}'.format(a,b,sum))

#Difference of two number
diff=int(a)-int(b)
print('difference of {0} and {1} is {2}'.format(a,b,diff))

#Product of two number
product=int(a)*int(b)
print('Product of {0} and {1} is {2}'.format(a,b,product))

